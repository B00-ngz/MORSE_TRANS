/* USER CODE BEGIN 4 */
void GenerateSine(void)
{
    for (int i = 0; i < TABLE_SIZE; i++)
    {
        float t = (float)i / TABLE_SIZE;
        float val = (sinf(2 * M_PI * t) + 1.0f) * 2047; // 12-bit DAC

        dac_buffer[i] = (uint16_t)val;
    }
}

void SetTone(uint8_t on)
{
    for (int i = 0; i < TABLE_SIZE; i++)
    {
        if (on)
            dac_buffer[i] = (uint16_t)((sinf(2 * M_PI * i / TABLE_SIZE) + 1.0f) * 2047);
        else
            dac_buffer[i] = 2048; // silence (mid-scale)
    }
}

void Morse_SOS(void)
{
    // S: ...
    for(int i=0;i<3;i++){
        SetTone(1);
        HAL_Delay(DOT_TIME_MS);
        SetTone(0);
        HAL_Delay(DOT_TIME_MS);
    }

    HAL_Delay(2 * DOT_TIME_MS);

    // O: ---
    for(int i=0;i<3;i++){
        SetTone(1);
        HAL_Delay(DASH_TIME_MS);
        SetTone(0);
        HAL_Delay(DOT_TIME_MS);
    }

    HAL_Delay(2 * DOT_TIME_MS);

    // S: ...
    for(int i=0;i<3;i++){
        SetTone(1);
        HAL_Delay(DOT_TIME_MS);
        SetTone(0);
        HAL_Delay(DOT_TIME_MS);
    }
}


